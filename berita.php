			<li><h3> Beberapa langkah yang bisa dilakukan untuk mencegah infeksi virus Corona,
					 di antaranya:</h3>
			</ul>
			</ul>
				<p>1. Mencuci tangan dengan benar
					Mencuci tangan dengan benar adalah cara paling sederhana namun efektif untuk 
					mencegah penyebaran virus 2019-nCoV. Cucilah tangan dengan air mengalir dan sabun,
					setidaknya selama 20 detik. Pastikan seluruh bagian tangan tercuci hingga bersih,
					termasuk punggung tangan, pergelangan tangan, sela-sela jari, dan kuku. Setelah itu,
					keringkan tangan menggunakan tisu, handuk bersih, atau mesin pengering tangan.
					Jika Anda adalah pekerja komuter yang berada di dalam transportasi umum, akan 
					sulit untuk menemukan air dan sabun. Anda bisa membersihkan tangan dengan hand
					sanitizer. Gunakan produk hand sanitizer dengan kandungan alkohol minimal 60% 
					agar lebih efektif membasmi kuman.
					Cucilah tangan secara teratur, terutama sebelum dan setelah makan, setelah
					menggunakan toilet, setelah menyentuh hewan, membuang sampah, serta setelah
					batuk atau bersin. Cuci tangan juga penting dilakukan sebelum menyusui bayi atau
					memerah ASI.</p>
				<p>2. Menggunakan masker
					Banyak yang menggunakan masker kain untuk mencegah infeksi virus Corona, 
					padahal masker tersebut belum tentu efektif. Secara umum, ada dua tipe masker
					yang bisa Anda digunakan untuk mencegah penularan virus Corona, yaitu masker 
					bedah dan masker N95.
					Cara pakai masker bedah yang benar adalah sisi berwarna pada masker harus 
					menghadap ke luar, sementara sisi dalamnya yang berwarna putih menghadap wajah 
					dan menutupi dagu, hidung, dan mulut. Sisi berwarna putih terbuat dari material 
					yang dapat menyerap kotoran dan menyaring kuman dari udara.
					Meski tidak sepenuhnya efektif mencegah paparan kuman, namun penggunaan masker
					ini tetap bisa menurunkan risiko penyebaran penyakit infeksi, termasuk infeksi
					virus Corona. Penggunaan masker lebih disarankan bagi orang yang sedang sakit 
					untuk mencegah penyebaran virus dan kuman, ketimbang pada orang yang sehat.</p>
				<p>3. Menjaga daya tahan tubuh
					Daya tahan tubuh yang kuat dapat mencegah munculnya berbagai macam penyakit. 
					Untuk menjaga dan meningkatkan daya tahan tubuh, Anda disarankan untuk 
					mengonsumsi makanan sehat, seperti sayuran dan buah-buahan, dan makanan
					berprotein, seperti telur, ikan, dan daging tanpa lemak. Bila perlu, Anda 
					juga menambah konsumsi suplemen sesuai anjuran dokter.
					Selain itu, rutin berolahraga, tidur yang cukup, tidak merokok, dan tidak 
					mengonsumsi minuman beralkohol juga bisa meningkatkan daya tahan tubuh agar 
					terhindar dari penularan virus Corona.
					Beberapa informasi menyebutkan bahwa berjemur di bawah sinar matahari juga dapat
					mencegah infeksi virus Corona. Sayangnya hal ini masih perlu diteliti lebih lanjut.</p>
				<p>4. Tidak pergi ke negara terjangkit
					Tidak hanya Tiongkok, penyakit infeksi virus Corona kini juga sudah mewabah 
					ke beberapa negara lain, seperti Jepang, Korea Selatan, Hongkong, Taiwan, India,
					Amerika Serikat, dan Eropa. Virus Corona juga sudah terkonfirmasi ditemukan di
